    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tabelmodel = new DefaultTableModel();
        tabelmodel.addColumn("id_masakan");
        tabelmodel.addColumn("nama_masakan");
        tabelmodel.addColumn("size");
        tabelmodel.addColumn("harga");
        tabelmodel.addColumn("status");
        String kode = tfcari.getText();
        String query = "select * from snack where nama_snack like '%"+kode+"%'";
        
        try {
            stm.executeQuery(query);
            ResultSet rs = stm.executeQuery(query);
            while (rs.next()) {
                tabelmodel.addRow(new Object[]{rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5)});
            }
            tbmakanan.setModel(tabelmodel);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "error:"+e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

