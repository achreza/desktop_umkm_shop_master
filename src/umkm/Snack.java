    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        // TODO add your handling code here:
        String id = tfidmakanan.getText();
        int confirmed = JOptionPane.showConfirmDialog(this, "Yakin menghapus?", "ya",JOptionPane.YES_NO_OPTION);
        if (confirmed == JOptionPane.YES_OPTION){
            String query = "DELETE FROM snack WHERE id_snack = '"+id+"'";
            try {
                stm.executeUpdate(query);
                this.resetText();
                this.resetTable();
                this.selectMakanan();
            } catch (SQLException ex) {
                Logger.getLogger(Snack.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnhapusActionPerformed

